package modelo;

public class TelProv {
    String tel;
    int id;
    
    public TelProv() {
    }

    public TelProv(String tel, int id) {
        this.tel = tel;
        this.id = id;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
