    void listar() {
        List<Categoria> lista = catdao.listar();
        
        modelo = (DefaultTableModel) tabla.getModel();
        
        Object[] ob = new Object[9];
        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getId();
            
            ob[1] = lista.get(i).getNombre();
            modelo.addRow(ob);
        }
        tabla.setModel(modelo);
    }
